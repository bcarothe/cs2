#include "string.hpp"
#include <cassert>
#include <iostream>
//============================================================

int main ()
{
    { //------------------------------------------------------
        // SETUP FIXTURE
        String str;
        
        // VERIFY
        assert(str == '\0');
    } //------------------------------------------------------
    { //------------------------------------------------------
        // SETUP FIXTURE
        String str('x');
        
        // VERIFY
        assert(str == 'x');
    } //------------------------------------------------------
    { //------------------------------------------------------
        // SETUP FIXTURE
        String str('0');
        
        // VERIFY
        assert(str == '0');
    } //------------------------------------------------------
    
    std::cout << "==========[ END OF CHARACTER CONSTRUCTOR TEST ]==========\n";
}

